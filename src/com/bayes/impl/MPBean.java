package com.bayes.impl;

public class MPBean {
     private double p;
     private int c;
     public MPBean(double p,int c){
    	 this.p = p;
    	 this.c = c;
     }
	public double getP() {
		return p;
	}
	public void setP(double p) {
		this.p = p;
	}
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
     
}
